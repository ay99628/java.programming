public class task3 {
    public static void main(String[] args) {


// 使用 Math.ceil()
        System.out.println("Math.ceil(2.1) = " + Math.ceil(2.1)); // 3.0
        System.out.println("Math.ceil(2.0) = " + Math.ceil(2.0)); // 2.0
        System.out.println("Math.ceil(-2.0) = " + Math.ceil(-2.0)); // -2.0
        System.out.println("Math.ceil(-2.1) = " + Math.ceil(-2.1)); // -2.0

// 使用 Math.floor()
        System.out.println("Math.floor(2.0) = " + Math.floor(2.0)); // 2.0
        System.out.println("Math.floor(2.1) = " + Math.floor(2.1)); // 2.0
        System.out.println("Math.floor(-2.0) = " + Math.floor(-2.0)); // -2.0
        System.out.println("Math.floor(-2.1) = " + Math.floor(-2.1)); // -3.0

// 使用 Math.rint()
        System.out.println("Math.rint(2.1) = " + Math.rint(2.1)); // 2.0
        System.out.println("Math.rint(-2.0) = " + Math.rint(-2.0)); // -2.0
        System.out.println("Math.rint(-2.1) = " + Math.rint(-2.1)); // -2.0
        System.out.println("Math.rint(2.5) = " + Math.rint(2.5)); // 2.0
        System.out.println("Math.rint(3.5) = " + Math.rint(3.5)); // 4.0
        System.out.println("Math.rint(-2.5) = " + Math.rint(-2.5)); // -2.0

// 使用 Math.round()
        System.out.println("Math.round(2.6f) = " + Math.round(2.6f)); // 3
        System.out.println("Math.round(2.0) = " + Math.round(2.0)); // 2
        System.out.println("Math.round(-2.0f) = " + Math.round(-2.0f)); // -2
        System.out.println("Math.round(-2.6) = " + Math.round(-2.6)); // -3
        System.out.println("Math.round(-2.4) = " + Math.round(-2.4)); // -2
    }
}



